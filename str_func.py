#!/usr/bin/python3

def all_upper(string):
    """
    Эта функция получает строку и возвращает её в верхнем регистре 0987654321
    """

    upper_string = string.upper()

    return upper_string


def all_title(string):
    """
    Эта функция получает строку и возвращает её так что каждая первая буква в слове будет написана в верхнем регистре
    """

    titled_string = string.title()

    return titled_string
